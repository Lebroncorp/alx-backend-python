def concat(str1: str, str2: str) -> str:
    """
    Type-annotated function that takes a string str1 and concatenates
    it to a second string str2 and returns a concatenated string str.

    Args:
        str1 (str): first string to concatenate.
        str2 (str): second string to concatenate.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
